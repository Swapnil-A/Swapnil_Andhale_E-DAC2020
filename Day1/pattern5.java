package Day1;
class Pattern5
{
    public static void main (String args[])
    {
        int odd=1;
        int sp=8;
        for (int i=0;i<9;i++)
        {
            for (int j=1;j<=sp;j++);
            {
                System.out.print(" * ");
            }
        }
        
        for (int k=1;k<odd;k++)
        {
            System.out.print(" ");
        }
        System.out.println();
           
    }
}