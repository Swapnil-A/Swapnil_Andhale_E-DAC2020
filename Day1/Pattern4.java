package Day1;
class Pattern4
{
    public static void main(String args[])
    {
        int odd=1;
        int sp=8;
        for(int i=1;i<9;i++)
        {
            System.out.print(" ");
        }
        sp--;
        for(int k=9;k>odd;k--)
        {
            System.out.print(k+" ");
        }
        System.out.println();
           
               
    }
}